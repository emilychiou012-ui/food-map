// 匯入函式（依店名防重複）
function importRestaurants() {
  const existing      = JSON.parse(localStorage.getItem('foodList')) || [];
  const existingNames = new Set(existing.map(r => r.name));
  const now           = new Date().toLocaleDateString('zh-TW');

  const newItems = restaurantData
    .filter(r => !existingNames.has(r.name))
    .map((r, i) => ({
      id: Date.now() + i + 1,
      ...r,
      nextBooking: null,
      bookingHistory: [],
      createdAt: now
    }));

  localStorage.setItem('foodList', JSON.stringify([...newItems, ...existing]));
}
